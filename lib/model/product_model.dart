class ProductModel {
  final String name;
  final String subTitle;
  final int reviews;
  final int stars;
  final int sizeInML;
  final double price;
  final String imagePath;

  ProductModel({
    required this.name,
    required this.subTitle,
    required this.reviews,
    required this.stars,
    required this.sizeInML,
    required this.price,
    required this.imagePath,
  });
}
